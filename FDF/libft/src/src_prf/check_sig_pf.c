#include "../../includes/libft.h"

void			check_sig_pf(t_tab *a, long long int num)
{
	if (num < 0)
	{
		a->sym = '-';
		a->sig = 1;
	}
	if (num >= 0)
	{
		if (a->obel_x[4] == 1)
		{
			a->sym = ' ';
			a->sig = 1;
		}
		if (a->obel_x[4] == 2)
		{
			a->sym = '+';
			a->sig = 1;
		}
	}
}
